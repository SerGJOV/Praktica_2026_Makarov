// Функция f(x) = e^x - x - 2
double f(double x)
{
    return exp(x) - x - 2;
}

// Производная f'(x) = e^x - 1
double f_производная(double x)
{
    return exp(x) - 1;
}

// Метод Ньютона для поиска корня
double метод_ньютона(double x0, double точность, int& итерации, bool& успех)
{
    double x = x0;
    double x_предыдущее;
    итерации = 0;
    успех = true;
    
    const int МАКС_ИТЕРАЦИЙ = 1000;
    
    cout << "\nИтерационный процесс метода Ньютона:" << endl;
    cout << "----------------------------------------" << endl;
    cout << "Начальное приближение x0 = " << x0 << endl;
    cout << "Точность eps = " << точность << endl;
    cout << "----------------------------------------" << endl;
    
    while (итерации < МАКС_ИТЕРАЦИЙ)
    {
        double fx = f(x);
        double fpx = f_производная(x);
        
        // Проверка деления на ноль
        if (fabs(fpx) < 1e-15)
        {
            cout << "Ошибка: производная близка к нулю! Метод расходится." << endl;
            успех = false;
            return x;
        }
        
        x_предыдущее = x;
        x = x - fx / fpx;
        итерации++;
        
        // Вывод текущей итерации
        cout << "Итерация " << итерации << ": x = " << fixed << setprecision(10) << x;
        cout << ", f(x) = " << scientific << setprecision(6) << f(x) << endl;
        
        // Проверка достижения точности
        if (fabs(x - x_предыдущее) < точность)
        {
            cout << "----------------------------------------" << endl;
            cout << "Достигнута точность по |x_(i+1) - x_i| < eps" << endl;
            return x;
        }
        
        if (fabs(f(x)) < точность)
        {
            cout << "----------------------------------------" << endl;
            cout << "Достигнута точность по |f(x)| < eps" << endl;
            return x;
        }
    }
    
    cout << "Предупреждение: достигнуто максимальное число итераций!" << endl;
    успех = false;
    return x;
}

// Поиск начальных приближений методом сканирования
void поиск_интервалов(double a, double b, double шаг)
{
    bool найден = false;
    double x1 = a;
    double f1 = f(x1);
    
    cout << "\nПоиск интервалов, содержащих корни:" << endl;
    cout << "Сканирование отрезка [" << a << ", " << b << "] с шагом " << шаг << endl;
    cout << "----------------------------------------" << endl;
    
    for (double x2 = a + шаг; x2 <= b; x2 += шаг)
    {
        double f2 = f(x2);
        
        if (f1 * f2 < 0)
        {
            cout << "Найден интервал: [" << x1 << ", " << x2 << "]" << endl;
            cout << "  f(" << x1 << ") = " << f1 << endl;
            cout << "  f(" << x2 << ") = " << f2 << endl;
            найден = true;
        }
        
        x1 = x2;
        f1 = f2;
    }
    
    if (!найден)
    {
        cout << "Интервалы с корнями не найдены!" << endl;
        cout << "Возможно, корней нет или они находятся за пределами отрезка." << endl;
    }
    cout << "----------------------------------------" << endl;
}

