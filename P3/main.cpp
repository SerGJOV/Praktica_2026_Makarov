    cout << "========================================" << endl;
    cout << "Уравнение: dy/dx = sin(x + y)" << endl;
    cout << "========================================" << endl;
    
    double x0, y0;  // Начальные условия
    double a, b;    // Интервал
    double h;       // Шаг
    
