// Явный метод Эйлера
void метод_эйлера(double x0, double y0, double a, double b, double h, 
                   double* x_массив, double* y_массив, int& num_точек)
{
    double x = x0;
    double y = y0;
    
    // Количество шагов
    int n = static_cast<int>((b - a) / h) + 2; // +2 для страховки
    int index = 0;
    
    // Запоминаем начальную точку
    x_массив[index] = x;
    y_массив[index] = y;
    index++;
    
    cout << "\nПроцесс решения методом Эйлера:" << endl;
    cout << "========================================" << endl;
    cout << "Шаг 0: x = " << fixed << setprecision(4) << x 
         << ", y = " << setprecision(6) << y << endl;
    
    int шаг = 0;
    const int МАКС_ШАГОВ = 10000;
    
    while (x < b && шаг < МАКС_ШАГОВ)
    {
        double y_новый = y + h * f(x, y);
        x += h;
        y = y_новый;
        шаг++;
        
        // Сохраняем точку
        x_массив[index] = x;
        y_массив[index] = y;
        index++;
        
        // Вывод каждого 10-го шага для больших интервалов
        if (шаг % 10 == 0 || шаг == 1 || (b - x) < h)
        {
            cout << "Шаг " << шаг << ": x = " << fixed << setprecision(4) << x 
                 << ", y = " << setprecision(6) << y << endl;
        }
    }
    
    num_точек = index;
    cout << "========================================" << endl;
    cout << "Всего шагов: " << шаг << endl;
}

// Вывод таблицы результатов
void вывод_таблицы(double* x_массив, double* y_массив, int num_точек, int шаг_вывода)
{
    cout << "\nТаблица значений решения:" << endl;
    cout << "========================================" << endl;
    cout << "   x          y(x)          f(x,y)" << endl;
    cout << "----------------------------------------" << endl;
    
    for (int i = 0; i < num_точек; i += шаг_вывода)
    {
        cout << fixed << setprecision(4) << setw(8) << x_массив[i] 
             << "  " << setprecision(6) << setw(12) << y_массив[i]
             << "  " << setprecision(6) << setw(12) << f(x_массив[i], y_массив[i]) << endl;
    }
    
    // Вывод последней точки, если она не попала в цикл
    if ((num_точек - 1) % шаг_вывода != 0)
    {
        int i = num_точек - 1;
        cout << fixed << setprecision(4) << setw(8) << x_массив[i] 
             << "  " << setprecision(6) << setw(12) << y_массив[i]
             << "  " << setprecision(6) << setw(12) << f(x_массив[i], y_массив[i]) << endl;
    }
    cout << "========================================" << endl;
}

