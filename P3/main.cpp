    cout << "Начальные условия: y(" << x0 << ") = " << y0 << endl;
    cout << "Интервал: [" << a << ", " << b << "]" << endl;
    cout << "Шаг: " << h << endl;
    cout << "Количество точек: " << num_точек << endl;
    cout << "Конечное значение: y(" << x_массив[num_точек-1] << ") = " 
         << y_массив[num_точек-1] << endl;
    cout << "========================================" << endl;
    
