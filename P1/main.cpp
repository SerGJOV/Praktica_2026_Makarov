#include <iostream>
#include <cmath>
#include <iomanip>
#include <windows.h>

using namespace std;

double f(double x)
{
    return cbrt(3 * x * x + 5 * x - 1);
}

double трапеция(double a, double b, int n)
{
    double h = (b - a) / n;
    double сумма = (f(a) + f(b)) / 2.0;

    for (int i = 1; i < n; i++)
    {
        double x = a + i * h;
        сумма += f(x);
    }

    return h * сумма;
}

double симпсон(double a, double b)
{
    double m = (a + b) / 2.0;
    return (b - a) / 6.0 * (f(a) + 4 * f(m) + f(b));
}

int main()
{
    // Установка UTF-8 для работы с PowerShell
    SetConsoleCP(65001);
    SetConsoleOutputCP(65001);
    
    double a, b;
    int n;

    cout << "Численное интегрирование" << endl;
    cout << "Функция: cbrt(3x^2 + 5x - 1)" << endl;
    cout << "Подынтегральное выражение: кубический корень из (3x^2 + 5x - 1)" << endl << endl;
    
    // Ввод границ интегрирования с проверкой
    while (true)
    {
        cout << "Введите нижний предел a: ";
        if (!(cin >> a))
        {
            cout << "Ошибка: введите числовое значение!" << endl;
            cin.clear();
            cin.ignore(10000, '\n');
            continue;
        }
        break;
    }
    
    while (true)
    {
        cout << "Введите верхний предел b: ";
        if (!(cin >> b))
        {
            cout << "Ошибка: введите числовое значение!" << endl;
            cin.clear();
            cin.ignore(10000, '\n');
            continue;
        }
        break;
    }

    // Проверка на равенство пределов
    if (a == b)
    {
        cout << "Ошибка: нижний и верхний пределы должны быть разными!" << endl;
        cout << "Интеграл на отрезке нулевой длины равен 0." << endl;
        system("pause");
        return 1;
    }
    
    // Автоматическая перестановка пределов, если a > b
    if (a > b)
    {
        cout << "Предупреждение: нижний предел больше верхнего. Меняем местами." << endl;
        double temp = a;
        a = b;
        b = temp;
    }
    
    // Ввод количества интервалов с проверкой
    while (true)
    {
        cout << "Введите количество интервалов n (четное для Симпсона): ";
        if (!(cin >> n))
        {
            cout << "Ошибка: введите целое число!" << endl;
            cin.clear();
            cin.ignore(10000, '\n');
            continue;
        }
        
        if (n <= 0)
        {
            cout << "Ошибка: количество интервалов должно быть положительным!" << endl;
            continue;
        }
        
        if (n > 1000000)
        {
            cout << "Предупреждение: слишком большое число интервалов может замедлить вычисления." << endl;
            cout << "Продолжить? (1 - да, 0 - нет): ";
            int choice;
            cin >> choice;
            if (choice == 0) continue;
        }
        
        break;
    }
    
    // Предупреждение о нечетном n для метода Симпсона
    if (n % 2 != 0)
    {
        cout << "Предупреждение: метод Симпсона требует четное число интервалов." << endl;
        cout << "Увеличиваю n до " << n + 1 << " для метода Симпсона." << endl;
    }
    
    cout << "\nВычисление интеграла..." << endl;
    cout << "Границы: [" << a << ", " << b << "]" << endl;
    cout << "Интервалов: " << n << endl << endl;
    
    // Проверка области определения функции
    double проверка_a = 3 * a * a + 5 * a - 1;
    double проверка_b = 3 * b * b + 5 * b - 1;
    
    if (проверка_a < 0 || проверка_b < 0)
    {
        cout << "Предупреждение: на границах отрезка подкоренное выражение отрицательно." << endl;
        cout << "Кубический корень существует, но результат может быть комплексным." << endl;
        cout << "Будет вычислен действительный корень (отрицательные значения)." << endl << endl;
    }
    
    double трап = трапеция(a, b, n);
    double симп = симпсон(a, b);

    cout << fixed << setprecision(10);
    cout << "Результаты:" << endl;
    cout << "Метод трапеций (n=" << n << ") : " << трап << endl;
    cout << "Метод Симпсона : " << симп << endl;
    
    // Оценка погрешности
    double погрешность = fabs(трап - симп);
    cout << "Разница между методами : " << погрешность << endl;
    
    if (погрешность > 0.1)
    {
        cout << "Предупреждение: большая разница между методами!" << endl;
        cout << "Рекомендуется увеличить количество интервалов." << endl;
    }
    
    cout << "\nПрограмма завершена успешно." << endl;
    system("pause");
    return 0;
}