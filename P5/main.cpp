// Структура для хранения точки
struct Точка
{
    double x;
    double y;
};

// Чтение данных из файла
bool чтение_данных(const string& имя_файла, Точка* точки, int& N)
{
    ifstream файл(имя_файла);
    
    if (!файл.is_open())
    {
        cout << "Ошибка: не удалось открыть файл " << имя_файла << endl;
        return false;
    }
    
    N = 0;
    while (файл >> точки[N].x >> точки[N].y)
    {
        N++;
        if (N >= 100)
        {
            cout << "Предупреждение: достигнуто максимальное количество точек (100)" << endl;
            break;
        }
    }
    
    файл.close();
    
    if (N == 0)
    {
        cout << "Ошибка: файл пуст или содержит некорректные данные!" << endl;
        return false;
    }
    
    cout << "Прочитано " << N << " точек из файла " << имя_файла << endl;
    return true;
}

